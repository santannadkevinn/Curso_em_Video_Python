import math
an = float(input('Digite um 창ngulo: '))
print('O 창ngulo de {} tem o SENO de {:.2f}'.format(an, math.sin(math.radians(an))))
print('O 창ngulo de {} o COSSENO de {:.2f}'.format(an, math.cos(math.radians(an))))
print('O 창ngulo de {} tem a TANGENTE de {:.2f}'.format(an, math.tan(math.radians(an))))